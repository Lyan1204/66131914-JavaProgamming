
public class KhaiBaoTInhToan {

	public static void main(String[] args) {
		//Khai báo biến
		int a = 12;
		int b = 4;
		double c = 2006;
		
		//Tính toán
		int Tong = a + b;
		double Thuong = c / a;
		int PhanDu = a;
		
		//In kết quả
		System.out.println("Tổng: " + Tong);
		System.out.println("Thương: " + Thuong);
		System.out.println("Phần dư: " + PhanDu);

	}

}
