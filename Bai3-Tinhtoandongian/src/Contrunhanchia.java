
public class Contrunhanchia {

	public static void main(String[] args) {
		double so1= 36;
		double so2= 18;
		System.out.println("Phép nhân: " + so1 + " x " + so2 + " = " + (so1 * so2));
		System.out.println("Phép chia: " + so1 + " : " + so2 + " = " + (so1 / so2));
		System.out.println("Phép cộng:  " + so1 + " + " + so2 + " = " + (so1 + so2));
		System.out.println("Phép trừ: " + so1 + " - " + so2 + " = " + (so1 - so2));
		

	}

}
